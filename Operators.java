
public class Operators {

    public static void main(String[] args) {
        /*
         * Operators in java Operand operator Operand = Result 4 + 4 = 8 Types of
         * operators in java Arthimatic operators Assignment operators Logical operators
         * Comparsion operators
         */
        // Arthimatic operator
        int num1 = 55, num2 = 65;
        // Assignment opertaor
        num1 += 10;
        System.out.println("the value of num1 + num2 is:");
        System.out.println(num1 + num2);
        System.out.println("the value of num1 - num2 is:");
        System.out.println(num1 - num2);
        System.out.println("the value of num1 * num2 is:");
        System.out.println(num1 * num2);
        System.out.println("the value of num1 / num2 is:");
        System.out.println(num1 / num2);
        System.out.print("the value of num1 % num2 is:");
        System.out.println(num1 % num2);

        System.out.println(num1++);
        System.out.println(++num1);
        System.out.println(num1--);
        System.out.println(--num1);

    }
}
